package com.embabel.database.server.controller

import org.springframework.http.HttpStatus
import org.springframework.http.ResponseEntity
import org.springframework.web.bind.annotation.ExceptionHandler
import org.springframework.web.bind.annotation.RestControllerAdvice

@RestControllerAdvice
class GlobalExceptionHandler {

    @ExceptionHandler(IllegalArgumentException::class)
    fun handleTimeout(ex: IllegalArgumentException): ResponseEntity<Map<String, String>> {
        val errorResponse = mapOf("error" to "Error retrieving results", "message" to ex.message.orEmpty())
        return ResponseEntity.status(HttpStatus.GATEWAY_TIMEOUT).body(errorResponse)
    }
}
